#!/usr/bin/env node
/**
 * Generate Ed25519 keypair and write ENV_COLLECTOR_PRIVATE_KEY_PEM_BASE64 to .env.local.
 * Then sync public key to API-side .env files. Never logs secret values.
 */
import fs from "fs";
import path from "path";
import crypto from "crypto";
import { fileURLToPath } from "url";
import { syncPublicKeyFromCollector, upsertEnvVar } from "./sync-public-key-from-collector.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ihlRoot = path.resolve(__dirname, "..");
const localEnvPath = path.join(ihlRoot, ".env.local");
const localEnvExample = path.join(ihlRoot, ".env.local.example");
const KEY = "ENV_COLLECTOR_PRIVATE_KEY_PEM_BASE64";

function ensureRootEnv() {
  if (fs.existsSync(localEnvPath)) return;
  if (fs.existsSync(localEnvExample)) {
    fs.copyFileSync(localEnvExample, localEnvPath);
    console.log("GENERATE_ED25519_KEY=CREATE env=.env.local from=.env.local.example");
    return;
  }
  fs.writeFileSync(localEnvPath, "# Generated by generate-ed25519-key.mjs\n", "utf8");
  console.log("GENERATE_ED25519_KEY=CREATE env=.env.local");
}

function writePrivateKeyBase64() {
  const { privateKey } = crypto.generateKeyPairSync("ed25519");
  const pem = privateKey.export({ type: "pkcs8", format: "pem" }).toString();
  const b64 = Buffer.from(pem, "utf8").toString("base64");
  upsertEnvVar(localEnvPath, KEY, b64);
}

ensureRootEnv();
writePrivateKeyBase64();
console.log("GENERATE_ED25519_KEY=PASS written=.env.local");

syncPublicKeyFromCollector();
